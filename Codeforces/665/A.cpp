using namespace std;
using ll = long long;

int solve()
{
  ll n,k;
  cin >> n >> k;

  if (n >= k){
    if ((n - k) % 2 == 0){
      cout << 0 << endl;
    }else{
      cout << 1 << endl;
    }
  }else{
    cout << k - n << endl;
  }
  return 0;
}

int main()
{
  // 整数の入力
  ll t;
  cin >> t;
  for (size_t i = 0; i < t; i++)
  {
    solve();
  }
  return 0;
}