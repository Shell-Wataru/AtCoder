using ll = long long;
const ll BASE_NUM = 1000000007;

vector<int> prime_numbers(int n){
    if (n <= 1){
        return vector<int>(0);
    }
    vector<int> ans = {2};
    for(int i = 3;i <= n;i++){
        bool is_prime = true;
        for(int j = 0; ans[j] * ans[j] <= i;j++){
            if (i % ans[j] == 0){
                is_prime = false;
                break;
            }
        }
        if (is_prime){
            ans.push_back(i);
        }
    }
    return ans;
}

vector<int> ps = prime_numbers(30000);
int solve()
{
  ll d;
  cin >> d;

  ll a,b;
  for(auto &p:ps){
    if(p > d){
      a = p;
      break;
    }
  }
  for(auto &p:ps){
    if(p >= a + d){
      b = p;
      break;
    }
  }
  cout << a*b << endl;
  return 0;
}
int main()
{
  ll t;
  cin >> t;
  for (int i = 1; i <= t; i++)
  {
    solve();
  }
  return 0;
}